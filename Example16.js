const student={
    firstName:"Pranay",
    rollNumber:32,
}

console.log(student);


// Acessing the Properties 
// using "." operator
console.log(student.firstName);
console.log(student.rollNumber);

// Using Bracket Notation 
console.log(student["firstName"]);
console.log(student["rollNumber"]);
